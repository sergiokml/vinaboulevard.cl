<h5>Sobre el pago de reservas</h5>
<p>Todas las reservas son cobradas en pesos chilenos, y al momento de la reserva, se efectúa el pre pago del 50%.</p>


<h5>Extranjeros</h5>
<p>Los pasajeros extranjeros que presenten su pasaporte y tarjeta de inmigración al país el día de su llegada estarán exentos de pagar I.V.A.</p>

<h5>Sobre las cancelaciones</h5>
<p>Para quienes cancelen hasta 7 días antes de su llegada, se hará devolución del total de lo abonado. En caso que cancelen pasado ese plazo se cobrará el 25% de la reserva.</p>

<h5>Servicios no cubiertos</h5>
<p>Nuetros departamentos no cuentan con servicio a la habitación.</p>